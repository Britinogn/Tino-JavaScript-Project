// for loop
for(let i = 1; i < 11; i++){
    if( i % 2 === 0){
        console.log(i + " is an even number ");
    }else{
        console.log(i + " is an odd number ");
    }
}

// while 
let num = 1;
while(num <11 ){
    if(num % 2 === 0){
        console.log(num + " is an even number ");
    }else{
          console.log(num + " is an odd number ");
    }
    num++
}


